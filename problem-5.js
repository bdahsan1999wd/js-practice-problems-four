var signal = 'yellow';

if (signal === 'red'){
    console.log('Stop!!! The signal is red.');
}
else if (signal === 'yellow'){
    console.log('Slow down!!! The signal is yellow.');
}
else if (signal === 'green'){
    console.log('Go Go Go!!! The signal is green.');
}
else {
    console.log('Unknown signal color...');
}